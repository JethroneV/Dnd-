--	HOW TO USE:



-- The AudioTrigger script under the Trigger can hold one sound to manage.


-- If you want multiple Audio Objects for one trigger, just copy the AudioTrigger script. 

-- (but keep the copy as child under the trigger)


-- NOTE: The audio objects have to be inside a clientcontext, but not necessaryly this one. 

-- (rightclick on the object and then "create network context" -> "new client context containing this")


-- The custom properties on the AudioTrigger script manage how the sound is triggered.

-- (Stopping takes priority over starting)


-- The Plattform is just for visualization and save to delete.



-- Good luck and have fun :)